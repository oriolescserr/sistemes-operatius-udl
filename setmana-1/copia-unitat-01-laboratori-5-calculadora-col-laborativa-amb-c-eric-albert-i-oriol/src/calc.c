#include <stdio.h>
#include <math.h>
#include "calc.h"

double suma(double a, double b) {
    return a + b;
}

double resta(double a, double b) {
    return a - b;
}

double multiplicacio (double a, double b) {
    return a * b;
}

double divisio (double a, double b) {
    return a / b;
}

double potencia(double a, double b) {
    return pow(a, b);
}

double arrel(double a) {
    return sqrt(a);
}